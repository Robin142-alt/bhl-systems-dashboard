function requireEnv(name: string) {
  const value = process.env[name]?.trim();
  if (!value) {
    throw new Error(`Missing required env var: ${name}`);
  }
  return value;
}

function makeSeedTag(namespace: string, key: string) {
  return `seed:${namespace}:${key}`;
}

function uniqueStrings(values: string[]) {
  return Array.from(new Set(values.filter(Boolean)));
}

function daysFromNow(days: number, hourLocal = 9, minuteLocal = 0) {
  const now = new Date();
  const value = new Date(now);
  value.setDate(value.getDate() + days);
  value.setHours(hourLocal, minuteLocal, 0, 0);
  return value;
}

function toKenyanPhone(msisdn: string) {
  const trimmed = msisdn.trim();
  if (trimmed.startsWith("+254")) return trimmed;
  if (trimmed.startsWith("254")) return `+${trimmed}`;
  if (trimmed.startsWith("0")) return `+254${trimmed.slice(1)}`;
  return trimmed;
}

function buildReceiptText(input: {
  authority: "KRA" | "NSSF" | "SHA";
  receiptNo: string;
  date: string;
  amountKes: number;
  payer: string;
  pin?: string;
  prn?: string;
}) {
  // Keep this plain text: it seeds the OCR/extraction pipeline even without PDFs.
  const lines: string[] = [];
  lines.push(`${input.authority} PAYMENT RECEIPT`);
  lines.push(`Receipt Number: ${input.receiptNo}`);
  if (input.prn) lines.push(`PRN: ${input.prn}`);
  lines.push(`Filing/Payment Date: ${input.date}`);
  lines.push(`Payer: ${input.payer}`);
  if (input.pin) lines.push(`PIN: ${input.pin}`);
  lines.push(`Amount: KES ${input.amountKes.toLocaleString("en-KE", { maximumFractionDigits: 2 })}`);
  lines.push(`Status: SUCCESS`);
  lines.push(`Generated by BHL demo seed`);
  return `${lines.join("\n")}\n`;
}

