async function ensureWorkItem(input: {
  namespace: string;
  key: string;
  organizationId: number;
  clientEntityId?: number | null;
  userId: number;
  title: string;
  category: string;
  deadline: Date;
  frequency: string;
  responsible: string;
  requiredDocumentLabel: string;
  status?: string;
  riskScore?: string | null;
}) {
  const seedTag = makeSeedTag(input.namespace, input.key);
  const namespaceTag = makeSeedTag(input.namespace, "namespace");

  const existing = await prisma.workItem.findFirst({
    where: {
      organizationId: input.organizationId,
      archivedAt: null,
      aiTags: { has: seedTag },
    },
    select: { id: true, aiTags: true },
  });

  const aiTags = uniqueStrings([namespaceTag, seedTag, ...(existing?.aiTags || [])]);

  if (existing) {
    return prisma.workItem.update({
      where: { id: existing.id },
      data: {
        title: input.title,
        category: input.category,
        deadline: input.deadline,
        frequency: input.frequency,
        responsible: input.responsible,
        requiredDocumentLabel: input.requiredDocumentLabel,
        status: input.status ?? "Pending",
        riskScore: input.riskScore ?? null,
        remindDaysBefore: 3,
        organizationId: input.organizationId,
        clientEntityId: input.clientEntityId ?? null,
        userId: input.userId,
        aiTags: { set: aiTags },
        aiPriorityIndex: 0.7,
      },
    });
  }

  return prisma.workItem.create({
    data: {
      title: input.title,
      category: input.category,
      deadline: input.deadline,
      frequency: input.frequency,
      responsible: input.responsible,
      requiredDocumentLabel: input.requiredDocumentLabel,
      status: input.status ?? "Pending",
      riskScore: input.riskScore ?? null,
      remindDaysBefore: 3,
      organizationId: input.organizationId,
      clientEntityId: input.clientEntityId ?? null,
      userId: input.userId,
      aiTags,
      aiPriorityIndex: 0.7,
    },
  });
}

async function replaceChecklist(workItemId: number, labels: string[]) {
  await prisma.workItemChecklistItem.deleteMany({ where: { workItemId } });
  await prisma.workItemChecklistItem.createMany({
    data: labels.map((label, idx) => ({
      workItemId,
      label,
      sortOrder: idx + 1,
      isDone: false,
    })),
  });
}

async function upsertApproval(input: {
  workItemId: number;
  submittedById: number;
  decidedById: number;
  status: "Submitted" | "Approved" | "Rejected";
  submissionNote?: string;
  managerResponseKind?: string;
  managerResponseLabel?: string;
  managerResponseNote?: string;
}) {
  const existing = await prisma.workItemApproval.findFirst({
    where: { workItemId: input.workItemId },
    orderBy: { submittedAt: "desc" },
    select: { id: true },
  });

  const base = {
    status: input.status,
    submissionNote: input.submissionNote ?? null,
    submittedById: input.submittedById,
    decidedById: input.status === "Submitted" ? null : input.decidedById,
    decidedAt: input.status === "Submitted" ? null : new Date(),
    rejectionReason: input.status === "Rejected" ? "Missing field mismatch needs clarification." : null,
    managerResponseKind: input.managerResponseKind ?? null,
    managerResponseLabel: input.managerResponseLabel ?? null,
    managerResponseNote: input.managerResponseNote ?? null,
    managerRespondedAt: input.managerResponseKind ? new Date() : null,
    managerRespondedById: input.managerResponseKind ? input.decidedById : null,
  };

  if (existing) {
    return prisma.workItemApproval.update({
      where: { id: existing.id },
      data: base,
    });
  }

  return prisma.workItemApproval.create({
    data: {
      workItemId: input.workItemId,
      ...base,
    },
  });
}

async function ensureDocument(input: {
  organizationId: number;
  clientEntityId?: number | null;
  ownerUserId: number;
  uploadedById: number;
  title: string;
  sourceType: string;
  fileName: string;
  folder: string;
  contentText: string;
}) : Promise<SeededDocument> {
  const existing = await prisma.document.findFirst({
    where: {
      organizationId: input.organizationId,
      archivedAt: null,
      title: input.title,
      sourceType: input.sourceType,
      ownerUserId: input.ownerUserId,
    },
    select: {
      id: true,
      versions: {
        select: { id: true, fileName: true, isCurrent: true, versionNumber: true },
        orderBy: [{ isCurrent: "desc" }, { versionNumber: "desc" }, { id: "desc" }],
        take: 1,
      },
    },
  });

  if (existing?.versions?.[0]) {
    const version = existing.versions[0];
    return {
      documentId: existing.id,
      documentVersionId: version.id,
      fileName: version.fileName,
      fileUrl: buildDocumentDownloadUrl(existing.id, version.id),
    };
  }

  const buffer = Buffer.from(input.contentText, "utf8");
  const registered = await registerDocumentUpload(prisma, {
    buffer,
    fileName: input.fileName,
    mimeType: "text/plain",
    folder: input.folder,
    title: input.title,
    sourceType: input.sourceType,
    organizationId: input.organizationId,
    clientEntityId: input.clientEntityId ?? null,
    ownerUserId: input.ownerUserId,
    uploadedById: input.uploadedById,
  });

  return {
    documentId: registered.documentId,
    documentVersionId: registered.documentVersionId,
    fileName: registered.fileName,
    fileUrl: registered.fileUrl,
  };
}

async function upsertEvidence(input: {
  workItemId: number;
  uploadedById: number;
  label: string;
  seededDocument: SeededDocument;
}) {
  const existing = await prisma.workItemEvidence.findFirst({
    where: {
      workItemId: input.workItemId,
      isCurrent: true,
      documentVersionId: input.seededDocument.documentVersionId,
    },
    select: { id: true },
  });

  if (existing) {
    return prisma.workItemEvidence.update({
      where: { id: existing.id },
      data: {
        label: input.label,
        fileUrl: input.seededDocument.fileUrl,
        fileName: input.seededDocument.fileName,
        documentId: input.seededDocument.documentId,
        documentVersionId: input.seededDocument.documentVersionId,
        uploadedById: input.uploadedById,
        isCurrent: true,
      },
    });
  }

  return prisma.workItemEvidence.create({
    data: {
      workItemId: input.workItemId,
      kind: "PROOF",
      label: input.label,
      fileUrl: input.seededDocument.fileUrl,
      fileName: input.seededDocument.fileName,
      documentId: input.seededDocument.documentId,
      documentVersionId: input.seededDocument.documentVersionId,
      uploadedById: input.uploadedById,
      isCurrent: true,
    },
  });
}

async function upsertVerification(input: {
  seededDocument: SeededDocument;
  workItemId: number;
  status: "MATCHED" | "MISMATCH" | "CHECK_NEEDED";
  summary: string;
  fields: Record<string, unknown>;
  mismatches?: Record<string, unknown>;
}) {
  return prisma.documentVerification.upsert({
    where: {
      documentVersionWorkItemVerification: {
        documentVersionId: input.seededDocument.documentVersionId,
        workItemId: input.workItemId,
        verificationSource: "SYSTEM",
      },
    },
    update: {
      status: input.status,
      summary: input.summary,
      fields: input.fields as Prisma.InputJsonValue,
      mismatches: input.mismatches
        ? (input.mismatches as Prisma.InputJsonValue)
        : Prisma.JsonNull,
      verifiedAt: new Date(),
    },
    create: {
      documentId: input.seededDocument.documentId,
      documentVersionId: input.seededDocument.documentVersionId,
      workItemId: input.workItemId,
      status: input.status,
      verificationSource: "SYSTEM",
      summary: input.summary,
      fields: input.fields as Prisma.InputJsonValue,
      mismatches: input.mismatches
        ? (input.mismatches as Prisma.InputJsonValue)
        : Prisma.JsonNull,
    },
  });
}

